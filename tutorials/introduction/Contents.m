% iset3d/tutorials/introduction
%
% Tutorial scripts that introduce iset3d. These are the introductory scripts, the place
% to start. The order below is a reasonable one to go through them in.
%
% t_piIntro_start               - Render a diffuse sphere through a pinhole camera.
% t_piIntro_teapot              - Render a scene more interesting than a sphere.
% t_piIntro_macbeth             - Render a MacBeth Color Checker Chart with
%                                   depth and illumination maps.
% t_piIntro_macbeth_zmap        - Render a MacBeth Color Checker Chart with zmap.
% t_piIntro_fisheyelens         - Render through a lens.
% t_piIntro_light               - Manipulate scene lighting.
% t_piIntro_simpletransforms    - Show how to rotate and translate an object.
% t_piIntro_material            - Show how to change object material
% t_piIntro_material_color      - Show how to change object color.
%
% t_piIntro_cameramotion        - Render with camera motion blur.
% t_piIntro_assetmotion         - Render with motion blur of one object.
%
% t_piIntro_rdt                 - Use RemoteDataToolbox to download and render a scene.